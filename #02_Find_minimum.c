#include <stdio.h>
#include <stdlib.h>
#include <time.h>
#define size 10

int main()
{
	// 10개의 정수를 저장할 수 있는 배열 선언
	// index를 표현할 변수와 최솟값을 저장할 변수 선언
	int num[10];
	int i, min;

	for (i = 0; i < size; i++)
	{
		num[i] = (rand() % 100) + 1;
		printf("%-3d ", num[i]);
	}
	printf("\n\n");

	// 최솟값을 우선 배열의 가장 첫 원소로 지정
	min = num[0];

	// 최솟값과 배열의 원소를 비교
	for (i = 0; i < size; i++) {
		if (min > num[i]) {
			min = num[i];
		}
	}

	// 최솟값 출력
	printf("최소값은 %d 입니다.\n", min);
}
