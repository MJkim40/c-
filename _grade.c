#include <stdio.h>

int main()
{
	int score;
	printf("점수를 입력하세요: ");
	scanf_s("%d", &score);

	//조건문 (if-else if-else)을 이용하여 학점 출력
	if (score > 100 || score<0) {
		printf("잘못된 점수입니다.");
	}
	else if (score >= 90) {
		printf("A학점");
	}
	else if (score >= 80) {
		printf("B학점");
	}
	else if (score >= 70) {
		printf("C학점");
	}
	else if (score >= 60) {
		printf("D학점");
	}
	else {
		printf("F학점");
	}
	return 0;
}
